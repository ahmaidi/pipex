#include "pipex.h"

int	ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	if ((char)c)
	{
		while (s[i] != '\0')
		{
			if (s[i] == (char)c)
				return (1);
			i++;
		}
		return (0);
	}
	else
		return (0);
}
